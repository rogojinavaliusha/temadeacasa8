int[,,] array = new int[2, 2, 2];
            int num = 10; // Начальное значение для двузначных чисел

            // Заполняем массив неповторяющимися двузначными числами
            for (int i = 0; i < 2; i++)
            {
                for (int j = 0; j < 2; j++)
