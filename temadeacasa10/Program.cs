/*Задайте прямоугольный двумерный массив. Напишите программу, которая будет находить строку с наименьшей суммой элементов.

Например, задан массив:
1 4 7 2
5 9 2 3
8 4 2 4
5 2 6 7
Программа считает сумму элементов в каждой строке и выдаёт номер строки с наименьшей суммой элементов: 1 строка */


        
            int[,] array = {
                {1, 4, 7, 2},
                {5, 9, 2, 3},
                {8, 4, 2, 4},
                {5, 2, 6, 7}
            };

            int rowCount = array.GetLength(0); // Количество строк
            int colCount = array.GetLength(1); // Количество столбцов

            int minSum = int.MaxValue; // Начальное значение минимальной суммы
            int minSumRowIndex = -1; // Начальное значение индекса строки с минимальной суммой

            // Находим строку с минимальной суммой элементов
            for (int i = 0; i < rowCount; i++)
            {
                int rowSum = 0;

                for (int j = 0; j < colCount; j++)
                {
                    rowSum += array[i, j];
                }

                if (rowSum < minSum)
                {
                    minSum = rowSum;
                    minSumRowIndex = i;
                }
            }

            // Выводим результат
            Console.WriteLine($"Строка с наименьшей суммой элементов: {minSumRowIndex + 1}");
            
        
    






